    private void formWindowGainedFocus(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowGainedFocus
        DefaultTableModel monModelTableauIntermittent = new DefaultTableModel();

        monModelTableauIntermittent.setDataVector(null,
                new Object[]{"Nom", "Prénom", "Détails", "Modifier", "Supprimer", "Spectacles"});

        jTableListeIntermittents.setModel(monModelTableauIntermittent);

        if (!NezDamesSerialisation.lesIntermittents.isEmpty()) { //s'il y a des intermittents
            
            for (int i = 0; i < NezDamesSerialisation.lesIntermittents.size(); i++) {
                String nom = NezDamesSerialisation.lesIntermittents.get(i).getNomIntermittent();
                String prenom = NezDamesSerialisation.lesIntermittents.get(i).getPrenomIntermittent();
                
                monModelTableauIntermittent.addRow(new Object[]{nom, prenom, "Détails", "Modifier", "Supprimer", "Spectacles"});
            }

            jTableListeIntermittents.getColumn("Détails").setCellRenderer(new ButtonRenderer());
            jTableListeIntermittents.getColumn("Détails").setCellEditor(new ButtonEditorDetails(new JCheckBox()));
            jTableListeIntermittents.getColumn("Modifier").setCellRenderer(new ButtonRenderer());
            jTableListeIntermittents.getColumn("Modifier").setCellEditor(new ButtonEditorModifier(new JCheckBox()));
            jTableListeIntermittents.getColumn("Supprimer").setCellRenderer(new ButtonRenderer());
            jTableListeIntermittents.getColumn("Supprimer").setCellEditor(new ButtonEditorSupprimer(new JCheckBox()));
            jTableListeIntermittents.getColumn("Spectacles").setCellRenderer(new ButtonRenderer());
            jTableListeIntermittents.getColumn("Spectacles").setCellEditor(new ButtonEditorListerSpectacles(new JCheckBox()));
            
            // on change la taille des 2 premières colonnes
            jTableListeIntermittents.getColumn("Nom").setPreferredWidth(120);
            jTableListeIntermittents.getColumn("Prénom").setPreferredWidth(120);
        } 
    }//GEN-LAST:event_formWindowGainedFocus
 
