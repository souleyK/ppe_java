    private void formWindowGainedFocus(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowGainedFocus
        //on vérifie qu'il y a des spectacles
        DefaultTableModel monModelTableauSpectacle = new DefaultTableModel(); //jTableListeSpectacles.getModel();//new DefaultTableModel();

        monModelTableauSpectacle.setDataVector(null,
                new Object[]{"Numéro", "Titre", "Détails", "Modifier", "Supprimer"});

        jTableListeSpectacles.setModel(monModelTableauSpectacle);

        if (!NezDamesSerialisation.lesSpectacles.isEmpty()) { //s'il y a pas des spectacles
            
            for (int i = 0; i < NezDamesSerialisation.lesSpectacles.size(); i++) {
                String numeroObjet = NezDamesSerialisation.lesSpectacles.get(i).getNumeroObjet();
                String titre = NezDamesSerialisation.lesSpectacles.get(i).getTitre();
                
                monModelTableauSpectacle.addRow(new Object[]{numeroObjet, titre, "Détails", "Modifier", "Supprimer"});
            }

            jTableListeSpectacles.getColumn("Détails").setCellRenderer(new ButtonRenderer());
            jTableListeSpectacles.getColumn("Détails").setCellEditor(new ButtonEditorDetails(new JCheckBox()));
            jTableListeSpectacles.getColumn("Modifier").setCellRenderer(new ButtonRenderer());
            jTableListeSpectacles.getColumn("Modifier").setCellEditor(new ButtonEditorModifier(new JCheckBox()));
            jTableListeSpectacles.getColumn("Supprimer").setCellRenderer(new ButtonRenderer());
            jTableListeSpectacles.getColumn("Supprimer").setCellEditor(new ButtonEditorSupprimer(new JCheckBox()));
            
            // on change la taille des 2 premières colonnes
            jTableListeSpectacles.getColumn("Numéro").setPreferredWidth(100);
            jTableListeSpectacles.getColumn("Titre").setPreferredWidth(140);
        } else {

        }
    }//GEN-LAST:event_formWindowGainedFocus

